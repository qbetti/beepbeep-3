package ca.uqac.lif.cep.io;

public class SourceInfo {

    private int id;
    private String sourceName;

    public SourceInfo() {

    }

    public int getId() {
        return id;
    }

    public String getSourceName() {
        return sourceName;
    }

    public void setId(int id) {
        this.id = id;
    }

    public void setSourceName(String sourceName) {
        this.sourceName = sourceName;
    }
}
